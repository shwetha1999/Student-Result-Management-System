    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
              // TODO add your handling code here://Save
              String rollno = jTextField1.getText();
              String phy = jTextField2.getText();
              String chemistry = jTextField3.getText();
              String maths = jTextField4.getText();
              String em = jTextField5.getText();
              String dbms = jTextField6.getText();
              
              try{
            //create instance with mysql driver class
            Class.forName("com.mysql.jdbc.Driver");
            //con object is trying to give connection with drivermanager(mysql)
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Abcd@032022");
            //to push data to database
            //create a sqlserverStatement object for seding sql statement into database createStatement();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno ='"+rollno+"'");
            if(rs.next())
            {
            st.executeUpdate("insert into result values('"+rollno+"','"+phy+"','"+chemistry+"','"+maths+"','"+em+"','"+dbms+"')");
            JOptionPane.showMessageDialog(null, "Data added Successfully!");
            setVisible(false);//close current form after update
            new adminIndex().setVisible(true);//open new form
            }
            else
            {
            JOptionPane.showMessageDialog(null, "Roll number doesn't exist");
            } 
            
        }
        catch(Exception e){
            //if connection not made with database show error
          JOptionPane.showMessageDialog(null,e.toString());  
            
        }
              
    }//GEN-LAST:event_jButton6ActionPerformed

