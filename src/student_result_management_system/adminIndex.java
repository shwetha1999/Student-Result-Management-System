    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here://create student
        String course = (String)jComboBox1.getSelectedItem();
        String branch= (String)jComboBox2.getSelectedItem();
        String rollno = jTextField1.getText();
        String name = jTextField2.getText();
        String gender = (String)jComboBox3.getSelectedItem();
        String fathername = jTextField3.getText();
        
        try{
            //create instance with mysql driver class
            Class.forName("com.mysql.jdbc.Driver");
            //con object is trying to give connection with drivermanager(mysql)
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Abcd@032022");
            //to push data to database
            //create a sqlserverStatement object for seding sql statement into database createStatement();
            Statement st = con.createStatement();
            st.executeUpdate("insert into student values('"+rollno+"','"+course+"','"+branch+"','"+name+"','"+gender+"','"+fathername+"')");
            JOptionPane.showMessageDialog(null, "Data Updated Successfully!");
            setVisible(false);//close current form after update
            new adminIndex().setVisible(true);//open new form
        }
        catch(Exception e){
            //if connection not made with database show error
          JOptionPane.showMessageDialog(null,e.toString());  
            
        }
        
        
    }//GEN-LAST:event_jButton6ActionPerformed

