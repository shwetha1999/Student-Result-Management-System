    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        //set the result comes from student as table.
         try{
            //create instance with mysql driver class
            Class.forName("com.mysql.jdbc.Driver");
            //con object is trying to give connection with drivermanager(mysql)
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Abcd@032022");
            //to push data to database
            //create a sqlserverStatement object for seding sql statement into database createStatement();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student");
            //to convert student into table form
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        }
        catch(Exception e){
            //if connection not made with database show error
          JOptionPane.showMessageDialog(null,e.toString());  
            
        }
        
    }//GEN-LAST:event_formComponentShown

